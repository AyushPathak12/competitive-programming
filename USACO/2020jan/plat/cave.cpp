int n, m;
char A[1000][1000];
int height[1000000];
int height2[1000000];
ull ans[1000000];

class UnionFind {
private:
    vi p;
public:
    UnionFind(int N) {
        p.assign(N, 0);
        for (int i = 0; i < N; i++) p[i] = i;
    }

    int findSet(int i) { return (p[i] == i) ? i : (p[i] = findSet(p[i])); }

    bool isSameSet(int i, int j) { return findSet(i) == findSet(j); }

    void unionSet(int i, int j) {
        if (!isSameSet(i, j)) {
            int x = findSet(i), y = findSet(j);
            p[y] = x;
        }
    }
};

int zip(int i, int j) {
    return i * m + j;
}

int main() {
    setupIO("cave");

    re(n, m);
    F0R(i, n) {
        F0R(j, m) {
            re(A[i][j]);
            height[zip(i, j)] = INF;
            height2[zip(i, j)] = INF;
        }
    }

    UnionFind UF(n * m);

    SET(ans, 1, n * m);

    F0Rd(i, n) {
        F0Rd(j, m) {
            if (A[i][j] != '.') continue;

            int a = i, b = j + 1;
            if (b < m && A[a][b] == '.') {
                UF.unionSet(zip(a, b), zip(i, j));
            }

            int x = UF.findSet(zip(i, j));

            a = i + 1, b = j;
            if (a < n && A[a][b] == '.') {
                int y = UF.findSet(zip(a, b));
                if (height[y] != i) {
                    ans[x] = (ans[x] * ans[y]) % MOD;
                    UF.unionSet(zip(i, j), zip(a, b));
                } else if (x != y) {
                    UF.unionSet(zip(a, b), zip(i, j));
                    ans[UF.findSet(zip(a, b))] = (ans[UF.findSet(zip(a, b))]*ans[x])%MOD;
                }
            }

            height[UF.findSet(zip(i, j))] = i;
        }
        F0Rd(j, m) {
            if (A[i][j] == '.') {
                int x = UF.findSet(zip(i, j));
                if (height2[x] > i) {
                    height2[x] = i;
                    ans[x]++;
                }
            }
        }
    }

    ull totAns = 1;
    F0R(i, n) {
        F0R(j, m) {
            if (A[i][j] == '.' && zip(i, j) == UF.findSet(zip(i, j))) {
                totAns = (totAns*ans[zip(i, j)])%MOD;
                ans[zip(i, j)] = 1;
            }
        }
    }
    ps(totAns);

    return 0;
}