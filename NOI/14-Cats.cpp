bool solve(ll x) {
    if (x <= 4) {
        if (x == 1 || x == 4) return 0;
        return 1;
    }
    ll biggestPow2 = 1;
    while (biggestPow2 < x) biggestPow2 *= 2;
    if (x > biggestPow2/2) {
        return !solve(x - biggestPow2/2);
    } else {
        psD("shouldn't happen");
        exit(0);
    }
}

