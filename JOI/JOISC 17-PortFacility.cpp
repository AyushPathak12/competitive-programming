/*
 * - Each cargo container is a node. Edge between node if cargo container shipping time intersects another cargo container.
 * An edge basically means that the container can't be in the same side. Therefore our answer is 2^(# components).
 * - However if there is odd cycle then our answer is zero.
 *
 * Solve with DSU:
 * - Sort all the cargo containers from their start time.
 * - Maintain a map<int, int> pos that maps the end location of a container to its ID.
 * - Process cargo containers from left to right; to process, first we must union with all cargo containers
 *   that start before this one (which is handled by our sorting) and ends before this one ends but after this one begins.
 * - We can use lower bound on the map to figure out the first cargo container that ends after this one begins,
 *   union the cargo container, then increment iterator until we reach a container that ends after this one ends.
 * - However naively this is too slow; optimize by realizing that if the component we just unioned is in the same
 *   component as the last cargo container we are going to process, then we can just exit early
 * - To handle the odd cycle case, split each node into i and i+n. Then, when unioning (i, j), union (i, j+n)
 *   and (i+n, j). If at the end (i, i+n) is connected for any i, then our answer is zero.
 */

